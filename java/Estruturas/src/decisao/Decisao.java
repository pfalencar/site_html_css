package decisao;

public class Decisao {

	public static void main(String[] args) {
		
		int total = 10;
		if (total > 10) {
			System.out.println("O total é maior do que 10.");
		} else if (total == 10) {
			System.out.println("O total é igual a 10");
		} else {
			System.out.println("O total é menor do que 10");
		}
	}

}
